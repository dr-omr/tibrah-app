export function findSuggestedCategories(freeText: string): string[] {
    if (!freeText) return [];
    const normalizedText = freeText.toLowerCase();
    
    const scores: { id: string, score: number }[] = [];

    for (const [key, pathway] of Object.entries(clinicalPathways)) {
        if (key === 'other') continue;
        
        let score = 0;
        for (const keyword of pathway.keywords) {
            // Regex match to catch distinct boundaries or partials in Arabic gracefully
            if (normalizedText.includes(keyword)) {
                score++;
            }
        }

        if (score > 0) {
            scores.push({ id: key, score });
        }
    }

    scores.sort((a, b) => b.score - a.score);
    return scores.map(s => s.id);
}
